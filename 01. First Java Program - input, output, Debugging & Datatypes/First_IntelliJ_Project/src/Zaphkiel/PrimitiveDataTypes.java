package Zaphkiel;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        int num = 69;
        float decimalNum = 69.69f;
        char character = 'P';
        long largeNum = 6969696969696969696L;
        double largeDecimalNum = 6969696969696969696.6969696969696969696;
        boolean trueOrfalse = false;

        System.out.println(num+" "+decimalNum+" "+character+" "+largeNum+" "+largeDecimalNum+" "+trueOrfalse);
    }
}
